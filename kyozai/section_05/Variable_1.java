package text.section_05;

public class Variable_1 {
	public static void main(String[] args) {
		
		// 変数testを宣言
		int test = 123;
		
		// 変数testの値を表示
		System.out.println(test);
	}
}

// Javaでは、変数を入れる箱の「型」と「変数名」の両方を定義する必要があります！
	// 型の定義は、[データ型] [変数名];　のように書きます。 → int test;
	// 変数の宣言時にデータを代入することも可能です。これを[初期化]と呼びます。
	//[データ型] [変数名] = [変数の初期値]; のように書きます。 → int test = 123;
	